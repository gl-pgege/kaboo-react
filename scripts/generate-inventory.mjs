#!/usr/bin/env node
/**
 * Generates docs/api-inventory.md — a flat, committed index of every public
 * symbol across BOTH barrels, derived from the TypeDoc markdown output in
 * docs/api. Run automatically by `yarn docs:api` (after `typedoc`).
 *
 * Zero dependencies; deterministic (sorted) so CI can diff it for drift.
 */
import { readdirSync, existsSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = new URL("..", import.meta.url).pathname;
const API_DIR = join(ROOT, "docs", "api");
const OUT = join(ROOT, "docs", "api-inventory.md");

const BARRELS = [
  { dir: "index", pkg: "@kaboo/react" },
  { dir: "copilotkit", pkg: "@kaboo/react/copilotkit" },
];

const KINDS = [
  { dir: "functions", label: "Functions / components / hooks" },
  { dir: "variables", label: "Variables" },
  { dir: "interfaces", label: "Interfaces" },
  { dir: "type-aliases", label: "Type aliases" },
];

function listSymbols(barrelDir, kindDir) {
  const dir = join(API_DIR, barrelDir, kindDir);
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => f.endsWith(".md"))
    .map((f) => f.replace(/\.md$/, ""))
    .sort((a, b) => a.localeCompare(b));
}

let out = "# API inventory\n\n";
out +=
  "Generated by `yarn docs:api` from the TypeDoc output. Every public export " +
  "of both barrels is listed here. Do not edit by hand.\n";

for (const barrel of BARRELS) {
  out += `\n## \`${barrel.pkg}\`\n`;
  for (const kind of KINDS) {
    const symbols = listSymbols(barrel.dir, kind.dir);
    if (symbols.length === 0) continue;
    out += `\n### ${kind.label}\n\n`;
    for (const name of symbols) {
      out += `- [\`${name}\`](api/${barrel.dir}/${kind.dir}/${name}.md)\n`;
    }
  }
}

writeFileSync(OUT, out);
console.log(`Wrote ${OUT}`);
